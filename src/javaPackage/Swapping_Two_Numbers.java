package javaPackage;
public class Swapping_Two_Numbers {
	public static void main(String[] args) {
		int x = 32;
		int y = 53;
		x = x+y;
		y = x-y;
		x = x-y;
		System.out.println("x = " + x + " and y = " + y );
		
		int a = 100;
		int b = 200;
		int c;
		c=a;
		a=b;
		b=c;
		System.out.println("a = " + a + " and b = " + b);
	}
}
